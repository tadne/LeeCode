    public static boolean search(int[] nums, int target) {
        int num=nums[0];
        int l=0;
        int r=nums.length-1;
        while (r>=l){
            int mid=(r-l)/2+l;//奇数出现全部相等
            if (l==r) return nums[l]==target;//偶数出现全相等
            else if (r==l+1) return nums[l]==target||nums[l+1]==target;
            else if (nums[mid]==nums[l]&&nums[mid]==nums[r]){//如果前后相等就没办法判断增减顺序,直接遍历
                l++;
                r--;
            }else if (nums[mid]>=nums[0]){//左边升序
                if (BinarySearch(nums,target,l,mid)) return true;
                l=mid+1;
            }else if (nums[mid]<=nums[r]){//右边降序
                if (BinarySearch(nums,target,mid,r)) return true;
                r=mid-1;
            }
        }
        return false;
    }
    public static boolean BinarySearch(int[] nums, int target,int l,int r){
        while (r>=l){
            int mid=(r-l)/2+l;
            if (target>nums[mid]){
                l=mid+1;
            }else if (target<nums[mid]){
                r=mid-1;
            }else return true;
        }
        return false;
    }



